package Semana_2.OperadoresAtribuição;

public class Ex5 {
    public static void main(String[] args) {
        /*
        Declarar uma variável double preço = 150.0;
        e usar o operador de atribuição de multiplicação =
        para aplicar um desconto de 10%.
         */
        double preco = 150.0;
        preco *= 0.9;
        System.out.println("O preço com desconto é: " + preco);
    }
}
