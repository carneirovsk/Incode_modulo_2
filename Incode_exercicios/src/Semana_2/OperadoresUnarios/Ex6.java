package Semana_2.OperadoresUnarios;

public class Ex6 {
    public static void main(String[] args) {
        /*
        Declarar uma variável int x = 10;
        e demonstrar o uso do operador de incremento e decremento pré e pós.
         */
        int x = 10;
        System.out.println("Incremento pós: " + x++);
        System.out.println("Incremento pré:" + ++x);
        System.out.println("Decremento pós: " + x--);
        System.out.println("Decremento pré: " + --x);




    }
}
